   ErrMsg  = ""
   DstParamData%HdroAdMsI = SrcParamData%HdroAdMsI
   DstParamData%HdroSttc = SrcParamData%HdroSttc
   DstParamData%PtfmVol0 = SrcParamData%PtfmVol0
