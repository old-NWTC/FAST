  ENDIF
  IF(ALLOCATED(Db_SS_Rdtn_Buf)) THEN
    IF ( .NOT. OnlySize ) DbKiBuf( Db_Xferred:Db_Xferred+SIZE(Db_SS_Rdtn_Buf)-1 ) = Db_SS_Rdtn_Buf
