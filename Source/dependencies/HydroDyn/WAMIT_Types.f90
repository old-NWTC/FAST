   i2_u = UBOUND(SrcParamData%WaveExctn,2)
   IF (.NOT. ALLOCATED(DstParamData%WaveExctn)) THEN 
      ALLOCATE(DstParamData%WaveExctn(i1_l:i1_u,i2_l:i2_u),STAT=ErrStat)
      IF (ErrStat /= 0) THEN 
         ErrStat = ErrID_Fatal 
         ErrMsg = 'WAMIT_CopyParam: Error allocating DstParamData%WaveExctn.'
         RETURN
      END IF
   END IF
   DstParamData%WaveExctn = SrcParamData%WaveExctn
ENDIF
   DstParamData%RhoXg = SrcParamData%RhoXg
IF (ALLOCATED(SrcParamData%WaveTime)) THEN
   i1_l = LBOUND(SrcParamData%WaveTime,1)
   i1_u = UBOUND(SrcParamData%WaveTime,1)
   IF (.NOT. ALLOCATED(DstParamData%WaveTime)) THEN 
      ALLOCATE(DstParamData%WaveTime(i1_l:i1_u),STAT=ErrStat)
      IF (ErrStat /= 0) THEN 
         ErrStat = ErrID_Fatal 
         ErrMsg = 'WAMIT_CopyParam: Error allocating DstParamData%WaveTime.'
         RETURN
      END IF
   END IF
   DstParamData%WaveTime = SrcParamData%WaveTime
ENDIF
   DstParamData%NStepWave = SrcParamData%NStepWave
      CALL Conv_Rdtn_CopyParam( SrcParamData%Conv_Rdtn, DstParamData%Conv_Rdtn, CtrlCode, ErrStat, ErrMsg )
      CALL SS_Rad_CopyParam( SrcParamData%SS_Rdtn, DstParamData%SS_Rdtn, CtrlCode, ErrStat, ErrMsg )
