    Re_Xferred   = Re_Xferred   + SIZE(InData%TData)
  ENDIF
  IF ( ALLOCATED(InData%DELTA) ) THEN
    IF ( .NOT. OnlySize ) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%DELTA))-1 ) =  PACK(InData%DELTA ,.TRUE.)
    Re_Xferred   = Re_Xferred   + SIZE(InData%DELTA)
  ENDIF
