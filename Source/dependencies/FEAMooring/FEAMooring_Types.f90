    Re_Xferred   = Re_Xferred   + SIZE(InData%LineCI)
  ENDIF
  IF ( ALLOCATED(InData%LineCD) ) THEN
    IF ( .NOT. OnlySize ) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%LineCD))-1 ) =  PACK(InData%LineCD ,.TRUE.)
