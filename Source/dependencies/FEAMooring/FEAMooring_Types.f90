  ENDIF
  IF ( ALLOCATED(InData%LDiam) ) THEN
    IF ( .NOT. OnlySize ) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%LDiam))-1 ) =  PACK(InData%LDiam ,.TRUE.)
    Re_Xferred   = Re_Xferred   + SIZE(InData%LDiam)
  ENDIF
  IF ( ALLOCATED(InData%LUnstrLen) ) THEN
