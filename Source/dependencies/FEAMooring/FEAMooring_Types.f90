  ENDIF
  IF ( ALLOCATED(InData%LRadFair) ) THEN
    IF ( .NOT. OnlySize ) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%LRadFair))-1 ) =  PACK(InData%LRadFair ,.TRUE.)
